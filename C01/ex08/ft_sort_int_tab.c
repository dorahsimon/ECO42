void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int tab2[size];

	i = 0;
	while (i < size - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			tab2[i] = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = tab2[i];
			i = 0;
		}
		else
			i++;
	}
}
