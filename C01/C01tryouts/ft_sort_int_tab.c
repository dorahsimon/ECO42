void	ft_sort_int_tab(int *tab, int size)
{
	int *tab2[size];
	int i;

	i = 0;
	while (i <= size)
	{
		if (&tab[i] >= &tab[i + 1])
		{
			tab2[i] = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = tab2[i];
		}
		else
			i++;
	}
}
