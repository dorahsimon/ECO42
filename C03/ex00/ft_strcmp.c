int	ft_strcmp(char *s1, char *s2)
{
	int i;

	while (*s1 == *s2)
	{
		s1++;
		s2++;
	}
	if (*s1 != *s2)
	{
		i = (*s1 - *s2);
		return (i);
	}
	else
		return (0);
}
