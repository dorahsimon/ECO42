int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int v;
	int i;

	v = 0;
	while ((v <= n) && (*s1) && (*s1 == *s2))
	{
		s1++;
		s2++;
		n--;
	}
	if (n > 0)
	{
		i = (*s1 - *s2);
		return (i);
	}
	else
		return (0);
}
