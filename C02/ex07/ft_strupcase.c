char	*ft_strupcase(char *str)
{
	int		count;

	count = 0;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = (*str - 32);
		}
		count++;
		str++;
	}
	str = (str - count);
	return (str);
}
