int		ft_is_prime(int nb)
{
	long	i;

	i = 5;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (0);
	if (nb == 1 || nb == 3)
		return (1);
	else if (nb % 2 == 0 || nb % 3 == 0)
		return (0);
	while (i * i <= nb)
	{
		if (nb % i == 0 || nb % (i + 2) == 0)
			return (0);
		i = i + 6;
	}
	return (1);
}
