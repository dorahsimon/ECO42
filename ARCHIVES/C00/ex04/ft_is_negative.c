#include "unistd.h"

void	ft_putchar(int c)
{
	write(1, &c, 1);
}

void	ft_is_negative(int n)
{
	int i;

	i = n;
	if (i < 0)
	{
		ft_putchar(78);
	}
	if (i >= 0)
	{
		ft_putchar(80);
	}
}
